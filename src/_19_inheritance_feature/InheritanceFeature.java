package _19_inheritance_feature;

public class InheritanceFeature {
    public static void main(String[] args) {
        /**
         1. 메서드 오버라이드(override)
            부모 클래스의 기능을 자식 클래스에서 재정의하여 사용한다.

         parent class(짜장 만드는 기능)
                        ↑ 상속
         child class(더 맛있는 짜장 만드는 기능)  ---객체 생성---> 객체1(parent class + child class)


         2. 자료형(타입)
            기본 자료형처럼 클래스도 자료형이다.


         3. Object 클래스
            모든 클래스의 최상위 클래스는 Object 클래스이다.


         4. super 클래스
            상위 클래스를 호출할 때 super 키워드를 이용한다.
         */
    }
}
