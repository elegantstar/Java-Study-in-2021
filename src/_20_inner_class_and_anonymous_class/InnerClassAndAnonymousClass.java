package _20_inner_class_and_anonymous_class;

public class InnerClassAndAnonymousClass {
    public static void main(String[] args) {
        /**
         1. 내부(inner) 클래스
            클래스 안에 또 다른 클래스를 선언하는 것으로, 이렇게 하면 두 클래스의 멤버에 쉽게 접근할 수 있다.
            그러나 클래스 단위로 구분되는 객체 지향 프로그래밍의 개념과 맞지 않기도 하며, 로직이 복잡해지고 코드가 장황해져 가독성이 떨어지기 때문에 실무에서는 거의 사용하지 않는다.


         2. 익명(anonymous) 클래스
            이름이 없는 클래스로, 주로 메서드를 재정의 하는 목적으로 사용한다.
            익명 클래스는 주로 인터페이스나 추상 클래스에서 사용된다.
         */
    }
}
